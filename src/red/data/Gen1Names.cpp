std::string_view SpeciesName(std::uint8_t id) { return kSpecies[id]; }

int PokedexNumber(std::uint8_t id) {
  for (std::size_t dex = 1; dex <= 151; ++dex)
    if (kPokedex[dex] == id) return static_cast<int>(dex);
  return -1;
}

bool IsValidSpecies(std::uint8_t id) {
  return PokedexNumber(id) > 0 && kSpecies[id] != "INVALID" &&
         kSpecies[id] != "MISSINGNO";
}

std::string_view MoveName(std::uint8_t id) { return kMoves[id]; }
bool IsValidMove(std::uint8_t id) {
  return id == 0 || (id <= 165 && kMoves[id] != "INVALID");
}
std::string_view ItemName(std::uint8_t id) { return kItems[id]; }
bool IsValidItem(std::uint8_t id) { return id != 0 && kItems[id] != "INVALID"; }
std::string_view MapName(std::uint8_t id) { return kMaps[id]; }

